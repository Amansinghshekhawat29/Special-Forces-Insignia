const knowMoreModal = document.getElementById("knowMoreModal");
const rankModal = document.getElementById("rankModal");
const knowMoreContent = document.getElementById("knowMoreContent");
const rankContent = document.getElementById("rankContent");

// Event for Know More
document.querySelectorAll(".know-more").forEach(button => {
  button.addEventListener("click", (e) => {
    const force = e.target.closest(".force-card").dataset.force;
    knowMoreContent.innerHTML = `<h2>${force.toUpperCase()}</h2><p>${forceData[force].description}</p>`;
    knowMoreModal.style.display = "block";
    document.body.style.overflow = "hidden";
  });
});

// Event for Rank
document.querySelectorAll(".view-rank").forEach(button => {
  button.addEventListener("click", (e) => {
    const force = e.target.closest(".force-card").dataset.force;
    rankContent.innerHTML = `<h2>${force.toUpperCase()} Ranks</h2>${forceData[force].ranks}`;
    rankModal.style.display = "block";
    document.body.style.overflow = "hidden";
  });
});

// Close Buttons
document.querySelectorAll(".close-btn").forEach(btn => {
  btn.addEventListener("click", (e) => {
    const modalId = e.target.getAttribute("data-close");
    document.getElementById(modalId).style.display = "none";
    document.body.style.overflow = "auto";
  });
});

// Outside Click to Close
window.onclick = function (event) {
  if (event.target.classList.contains("modal")) {
    event.target.style.display = "none";
    document.body.style.overflow = "auto";
  }
};




// countup.js

document.addEventListener("DOMContentLoaded", () => {
  const counters = document.querySelectorAll('.counter');
  const speed = 100; // Lower = faster animation

  const animateCounts = () => {
    counters.forEach(counter => {
      const updateCount = () => {
        const target = +counter.getAttribute('data-target');
        const count = +counter.innerText;

        const increment = Math.ceil(target / speed);

        if (count < target) {
          counter.innerText = count + increment;
          setTimeout(updateCount, 20);
        } else {
          counter.innerText = target.toLocaleString(); // Formatting
        }
      };
      updateCount();
    });
  };

  // Scroll trigger (runs only once)
  const section = document.getElementById('force-counts');
  let hasAnimated = false;

  window.addEventListener('scroll', () => {
    const sectionTop = section.getBoundingClientRect().top;
    const windowHeight = window.innerHeight;

    if (sectionTop < windowHeight && !hasAnimated) {
      animateCounts();
      hasAnimated = true;
    }
  });
});
